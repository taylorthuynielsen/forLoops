public class forLoops {

	public static void main(String[] args) 
	{
		challengeOne();
		challengeTwo();

	}
	
	public static void challengeOne()
	{
		for (int i=1; i<=5; i++)
		{
			System.out.println(i);
		}
	}
	
	public static void challengeTwo()
	{
		for (int i=2; i<=10; i=i+2)
		{
			System.out.println(i);
		}
	}

}
